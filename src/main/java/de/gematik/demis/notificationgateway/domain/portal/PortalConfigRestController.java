package de.gematik.demis.notificationgateway.domain.portal;

import static de.gematik.demis.notificationgateway.common.constants.WebConstants.API_NG_CONFIGS;

import lombok.RequiredArgsConstructor;
import org.springframework.cloud.sleuth.annotation.NewSpan;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequiredArgsConstructor
@RequestMapping(API_NG_CONFIGS)
public class PortalConfigRestController {
  private final PortalConfigProperties properties;

  @GetMapping("/portal")
  @NewSpan
  ResponseEntity<PortalConfigProperties> configProperties() {
    return ResponseEntity.ok(properties);
  }
}
